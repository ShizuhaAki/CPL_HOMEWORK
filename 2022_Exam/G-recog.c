int main()
{
    int u = -1, l = -1;
    for (int i = 0; i < 50; i++) {
        scanf("%s", picture[i]);
    }
    char str[SIZE] = {0};
    for (int i = 0; i < SIZE - 1; i++) {
        str[i] = '.';
    }
    printf("%s\n", str);
    for (int i = 0; i < 50; i++) {
        if (strcmp(str, picture[i]) != 0) {
            u = i;
            break;
        }
    }
    for (int i = u + 1; i < 50; i++) {
        if (strcmp(picture[i], str) == 0) {
            l = i - 1;
            break;
        }
    }
    printf("%d %d\n", u, l);
    return 0;
}